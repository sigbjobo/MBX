        double w12 = -9.721486914088159e-02;  // from MBpol
        double w13 = -9.721486914088159e-02;
        double wcross = 9.859272078406150e-02;

        monomer m;

        m.setup(A_1_a, w12, wcross, Z_1_a, Z_2_a);

        variable vr[21];

        v[0] = vr[0].v_exp(m_d_intra_AB, m_k_intra_AB, A_1_a, B_1_a);
