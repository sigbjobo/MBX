struct variable {
    double v_exp(const double& r0, const double& k, const double* xcrd, int o, int x);

    double v_coul(const double& r0, const double& k, const double* xcrd, int o, int x);

    void grads(const double& gg, double* xgrd, int o, int x) const;

    double g[3];  // diff(value, p1 - p2)
};

//- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - //

double variable::v_exp(const double& r0, const double& k, const double* xcrd, int o, int x) {
    g[0] = xcrd[o++] - xcrd[x++];
    g[1] = xcrd[o++] - xcrd[x++];
    g[2] = xcrd[o] - xcrd[x];

    const double r = std::sqrt(g[0] * g[0] + g[1] * g[1] + g[2] * g[2]);

    const double exp1 = std::exp(k * (r0 - r));
    const double gg = -k * exp1 / r;

    g[0] *= gg;
    g[1] *= gg;
    g[2] *= gg;

    return exp1;
}

//- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - //

double variable::v_coul(const double& r0, const double& k, const double* xcrd, int o, int x) {
    g[0] = xcrd[o++] - xcrd[x++];
    g[1] = xcrd[o++] - xcrd[x++];
    g[2] = xcrd[o] - xcrd[x];

    const double rsq = g[0] * g[0] + g[1] * g[1] + g[2] * g[2];
    const double r = std::sqrt(rsq);

    const double exp1 = std::exp(k * (r0 - r));
    const double rinv = 1.0 / r;
    const double val = exp1 * rinv;

    const double gg = -(k + rinv) * val * rinv;

    g[0] *= gg;
    g[1] *= gg;
    g[2] *= gg;

    return val;
}

//- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - //

void variable::grads(const double& gg, double* xgrd, int o, int x) const {
    for (int i = 0; i < 3; ++i) {
        const double d = gg * g[i];

        xgrd[o++] += d;
        xgrd[x++] -= d;
    }
}

